		  <h2>Personajes</h2>
		   	<p><strong> Luchador:</strong> Especializado en combate cercano. Combinan objetos de daño con objetos de aguante. También se los conoce como "offtanks" generalmente se pocisiona al frente en las batallas y su linea es top o jg</p>
				<p><strong> Tirador:</strong> Especializado en ataque a distancia. Popularmente conocido como "AD Carry" del inglés attack damage carry se pocisiona atras en la batalla para se prtotejido por su equipo, su linea es bot</p>
				<p><strong>Mago o Hechicero:</strong> Especializado en habilidades y daño mágico. Popularmente conocido como "AP Carry" del inglés ability power carry suele ir con el adc y su linea es mid</p>
				<p><strong>Apoyo o Soporte:</strong> Especializado en apoyar a su equipo y aportar visión en el mapa mediante guardianes de visión (wards).
				Un campeón puede tener más de un rol, para así complementar su equipo, dependiendo de sus características pueden usarse en diferentes posiciones de juego.</p> 
				<p><strong>asesino:</strong>Especializado en eliminar al jugador más importante del equipo enemigo (aunque ello suponga morir él también)</p>
				<p><strong>Tanque:</strong> Especializado en resistencia. Su misión es absorber la mayor cantidad de daño posible e iniciar las peleas.</p>
