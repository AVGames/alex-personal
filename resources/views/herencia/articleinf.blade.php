	    <h2>Personajes</h2>
		 	 <p><strong>Cole McGrath: </strong> Es el principal protagonista del juego. Adquirió poderes de manipulación eléctrica tras activar la esfera del rayo accidentalmente. Si escogemos el karma positivo, la gente nos ayudará a destruir a los segadores, los hombres de polvo y los primeros hijos. Si elegimos el karma negativo, la gente nos empezará a odiar.</p>

			<p><strong>Zeke Dunbar: </strong> Es el co-protagonista del juego. Mejor amigo de Cole. Ayuda a Cole a matar a Kessler.</p>

			<p><strong>Trish: </strong> Es el amor de Cole. Si eliges el karma negativo, ella te odiará y morirá por engaño de Kessler. Si eliges el positivo, te entenderá y morirá por la elección de Cole de salvar a mucha gente en lugar de a ella.</p>

			<p><strong >Kessler:</strong> Es el principal antagonista del juego. Roba la esfera a Alden y traiciona a todos, sólo para entrenar a Cole y para que pueda vencer a La Bestia. Es asesinado por Cole.</p>

			<p> <strong>Sasha:</strong> Es la novia de Kessler, jefa de los segadores. Cole consigue derrotarla. Durante el juego, una sustancia que implanta en Cole le envuelve en espejismos con su voz de fondo.</p>

			<p>  <strong>Alden:</strong> Jefe de los hombres de polvo. Destinado a ser el jefe de los primeros hijos.</p>

			<p> <strong>Moya Jones:</strong> Es una agente del FBI. Quiere ayudar a Cole, quien al final le cuenta toda la verdad.</p>
			<p><strong>John White: </strong> Agente de la NSA. Amigo de Cole. Muere a causa de una explosión provocada por Cole destruyendo o activando la esfera de rayos según se le da a escoger en cierta misión.</p>
